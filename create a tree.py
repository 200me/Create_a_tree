user_input1 = int(input("줄 수를 입력해주세요. :"))

for i in range(1, user_input1+1):
    print("*"*i)
                  
for i in range(1, user_input1+1):
    print(" "*(user_input1 - i) + ('*'*i))

for i in range(1, user_input1 +1):
    print(" " *(user_input1 - i) + ('*' * (2*i -1)))






























                        

